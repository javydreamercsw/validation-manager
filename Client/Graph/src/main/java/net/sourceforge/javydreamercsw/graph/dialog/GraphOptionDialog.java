    public GraphOptionDialog(ChartProvider cp, java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        initComponents();
        this.cp = cp;
        Map<Properties, Class> props = cp.getPropertyMap();
        Object[][] values = new Object[props.size()][2];
        int i = 0;
        for (Entry<Properties, Class> entry : props.entrySet()) {
            values[i][0] = entry.getKey();
            if (entry.getValue().equals(Integer.class)) {
                values[i][1] = 0;
            } else if (entry.getValue().equals(Boolean.class)) {
                values[i][1] = false;
            } else {
                values[i][1] = "";
            }
            i++;
        }
        jTable1.setModel(new javax.swing.table.DefaultTableModel(
                values,
                new String[]{
                    "Property", "Value"
                }
        ) {

            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return columnIndex == 1;
            }
        });
        jTable1.setDefaultRenderer(Object.class, new PanelCellEditorRenderer());
    }

    private class PanelCellEditorRenderer implements TableCellRenderer {

        private static final long serialVersionUID = 1L;

        @Override
        public Component getTableCellRendererComponent(JTable table, 
                Object value, boolean isSelected, boolean hasFocus, 
                int row, int column) {
            Component comp = new JLabel(value == null ? "" : value.toString());
            if (value instanceof Boolean) {
                Boolean aBoolean = (Boolean) value;
                comp = new JCheckBox();
                ((JCheckBox) comp).setSelected(aBoolean);
            } else if (value instanceof Integer) {
                Integer integer = (Integer) value;
                comp = new JLabel("" + integer);
            }
            return comp;
        }
    }

